// Función para obtener un consejo aleatorio
export function getRandomTip(): typeof SAFETY_TIPS[0] {
    return SAFETY_TIPS[Math.floor(Math.random() * SAFETY_TIPS.length)]
}

// Función para obtener un consejo por categoría
export function getTipByCategory(category: string): typeof SAFETY_TIPS[0] {
    const filtered = SAFETY_TIPS.filter(tip => tip.category === category)
    return filtered[Math.floor(Math.random() * filtered.length)]
}
